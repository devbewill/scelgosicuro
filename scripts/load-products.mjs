#!/usr/bin/env node
// Reads canonical product JSON from supabase/seed/products/*.json
// and generates a single idempotent SQL seed file: supabase/seed/seed_all.sql
//
// Run:  node scripts/load-products.mjs

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PRODUCTS_DIR = join(__dirname, '..', 'supabase', 'seed', 'products');
const OUT_FILE     = join(__dirname, '..', 'supabase', 'seed', 'seed_all.sql');

// --------------------------------------------------------------------------
// Sector mapping: slug → sector slug
// --------------------------------------------------------------------------
const SLUG_TO_SECTOR = {
  'amtrust-avvocato':              'avvocati',
  'amtrust-colpa-grave-extra':     'impiegati_pubblici',
  'amtrust-commercialista-protetto':'commercialisti',
  'amtrust-dentista-protetto':     'dentisti',
  'amtrust-farmacista-protetto':   'farmacisti',
  'amtrust-infortuni-h24':         'multisettore',
  'amtrust-ingegno-protetto':      'ingegneri_architetti',
  'amtrust-medico-protetto':       'medici',
  'amtrust-medico-under-35':       'medici',
  'amtrust-pubblico-impiego':      'impiegati_pubblici',
  'amtrust-sanitario-protetto':    'sanitari',
};

const SECTORS = [
  { slug:'medici',              name:'Medici',                      display_order:1 },
  { slug:'avvocati',            name:'Avvocati',                    display_order:2 },
  { slug:'commercialisti',      name:'Commercialisti',              display_order:3 },
  { slug:'ingegneri_architetti',name:'Ingegneri e Architetti',      display_order:4 },
  { slug:'sanitari',            name:'Professioni Sanitarie',       display_order:5 },
  { slug:'farmacisti',          name:'Farmacisti',                  display_order:6 },
  { slug:'dentisti',            name:'Dentisti',                    display_order:7 },
  { slug:'impiegati_pubblici',  name:'Dipendenti Pubblici',         display_order:8 },
  { slug:'multisettore',        name:'Multisettore',                display_order:9 },
];

const INSURERS = [
  { slug:'amtrust', name:'AmTrust Europe Limited' },
];

// --------------------------------------------------------------------------
// SQL helpers
// --------------------------------------------------------------------------
const esc  = v => v === null || v === undefined ? 'NULL' : `'${String(v).replace(/'/g,"''")}'`;
const num  = v => v === null || v === undefined ? 'NULL' : String(v);
const bool = v => v ? 'TRUE' : 'FALSE';
const json = v => v === null || v === undefined ? 'NULL' : `'${JSON.stringify(v).replace(/'/g,"''")}'::jsonb`;

// --------------------------------------------------------------------------
// Main
// --------------------------------------------------------------------------

const files = readdirSync(PRODUCTS_DIR)
  .filter(f => f.endsWith('.json') && !f.startsWith('_'))
  .sort();

const lines = [];
const push  = s => lines.push(s);

push('-- AUTO-GENERATED by scripts/load-products.mjs — do not edit by hand');
push('-- Run: node scripts/load-products.mjs && supabase db query --linked -f supabase/seed/seed_all.sql');
push('');
push('BEGIN;');
push('');

// ---- sectors ---------------------------------------------------------------
push('-- SECTORS');
for (const s of SECTORS) {
  push(`INSERT INTO sectors (slug, name, display_order, is_active)`);
  push(`VALUES (${esc(s.slug)}, ${esc(s.name)}, ${s.display_order}, TRUE)`);
  push(`ON CONFLICT (slug) DO UPDATE SET name = EXCLUDED.name, display_order = EXCLUDED.display_order;`);
}
push('');

// ---- insurers --------------------------------------------------------------
push('-- INSURERS');
for (const i of INSURERS) {
  push(`INSERT INTO insurers (slug, name) VALUES (${esc(i.slug)}, ${esc(i.name)})`);
  push(`ON CONFLICT (slug) DO UPDATE SET name = EXCLUDED.name;`);
}
push('');

// ---- collect all unique questions from all JSONs ---------------------------
push('-- QUESTIONS (global bank)');
const questionBank = new Map();
for (const file of files) {
  const d = JSON.parse(readFileSync(join(PRODUCTS_DIR, file), 'utf8'));
  for (const q of (d.questions || [])) {
    if (!questionBank.has(q.key)) questionBank.set(q.key, q);
  }
}
for (const q of questionBank.values()) {
  push(`INSERT INTO questions (key, label, help_text, type, options, validation)`);
  push(`VALUES (`);
  push(`  ${esc(q.key)},`);
  push(`  ${esc(q.label)},`);
  push(`  ${esc(q.help_text || null)},`);
  push(`  ${esc(q.type)},`);
  push(`  ${json(q.options || null)},`);
  push(`  ${json(q.validation || null)}`);
  push(`)`);
  push(`ON CONFLICT (key) DO UPDATE SET label = EXCLUDED.label, options = EXCLUDED.options, validation = EXCLUDED.validation;`);
}
push('');

// ---- per-product -----------------------------------------------------------
for (const file of files) {
  const d = JSON.parse(readFileSync(join(PRODUCTS_DIR, file), 'utf8'));
  const sectorSlug = SLUG_TO_SECTOR[d.slug];
  if (!sectorSlug) { console.warn(`⚠ No sector mapping for ${d.slug}`); continue; }

  push(`-- ================================================================`);
  push(`-- PRODUCT: ${d.slug}`);
  push(`-- ================================================================`);
  push('');

  // ---- sector_questions: link each question in this product to its sector -
  push(`-- sector_questions for sector '${sectorSlug}'`);
  const questions = d.questions || [];
  for (let i = 0; i < questions.length; i++) {
    const q = questions[i];
    push(`INSERT INTO sector_questions (sector_id, question_id, position, section, is_required, visible_if)`);
    push(`SELECT`);
    push(`  (SELECT id FROM sectors  WHERE slug = ${esc(sectorSlug)}),`);
    push(`  (SELECT id FROM questions WHERE key  = ${esc(q.key)}),`);
    push(`  ${i},`);
    push(`  ${esc(q.section || null)},`);
    push(`  ${bool(q.required !== false)},`);
    push(`  ${json(q.visible_if || null)}`);
    push(`ON CONFLICT (sector_id, question_id) DO NOTHING;`);
  }
  push('');

  // ---- product (delete + re-insert for idempotency) -----------------------
  push(`-- product row`);
  push(`DELETE FROM products WHERE slug = ${esc(d.slug)} AND insurer_id = (SELECT id FROM insurers WHERE slug = ${esc(d.insurer)});`);
  push(`INSERT INTO products (insurer_id, sector_id, slug, name, version, source_pdf, is_active)`);
  push(`VALUES (`);
  push(`  (SELECT id FROM insurers WHERE slug = ${esc(d.insurer)}),`);
  push(`  (SELECT id FROM sectors  WHERE slug = ${esc(sectorSlug)}),`);
  push(`  ${esc(d.slug)},`);
  push(`  ${esc(d.name)},`);
  push(`  ${esc(d.version)},`);
  push(`  ${esc(d.source_pdf || null)},`);
  push(`  TRUE`);
  push(`);`);
  push('');

  // ---- coverages + rate_rows + multipliers --------------------------------
  for (const cov of (d.coverages || [])) {
    push(`-- coverage: ${cov.key}`);
    push(`INSERT INTO product_coverages (product_id, key, name, is_mandatory, available_if, dimensions)`);
    push(`VALUES (`);
    push(`  (SELECT id FROM products WHERE slug = ${esc(d.slug)}),`);
    push(`  ${esc(cov.key)},`);
    push(`  ${esc(cov.name)},`);
    push(`  ${bool(cov.mandatory !== false)},`);
    push(`  ${json(cov.available_if || null)},`);
    push(`  ${json(cov.dimensions || [])}`);
    push(`);`);
    push('');

    push(`-- rate rows for coverage: ${cov.key}`);
    for (const r of (cov.rates || [])) {
      const { premium, manual_quote, ...dims } = r;
      push(`INSERT INTO product_rate_rows (coverage_id, dimension_values, premium, manual_quote)`);
      push(`VALUES (`);
      push(`  (SELECT id FROM product_coverages WHERE product_id = (SELECT id FROM products WHERE slug = ${esc(d.slug)}) AND key = ${esc(cov.key)}),`);
      push(`  ${json(dims)},`);
      push(`  ${num(premium || null)},`);
      push(`  ${bool(manual_quote || false)}`);
      push(`);`);
    }
    push('');

    // coverage-level multipliers
    for (const m of (cov.multipliers || [])) {
      const mName      = m.name || m.reason || 'multiplier';
      const mCondition = m.condition || m.if || {};
      push(`INSERT INTO product_multipliers (product_id, coverage_id, name, factor, condition, priority)`);
      push(`VALUES (`);
      push(`  (SELECT id FROM products WHERE slug = ${esc(d.slug)}),`);
      push(`  (SELECT id FROM product_coverages WHERE product_id = (SELECT id FROM products WHERE slug = ${esc(d.slug)}) AND key = ${esc(cov.key)}),`);
      push(`  ${esc(mName)},`);
      push(`  ${num(m.factor)},`);
      push(`  ${json(mCondition)},`);
      push(`  ${num(m.priority || 0)}`);
      push(`);`);
    }
    if ((cov.multipliers || []).length) push('');
  }

  // product-level multipliers (if any)
  for (const m of (d.multipliers || [])) {
    const mName      = m.name || m.reason || 'multiplier';
    const mCondition = m.condition || m.if || {};
    push(`INSERT INTO product_multipliers (product_id, coverage_id, name, factor, condition, priority)`);
    push(`VALUES (`);
    push(`  (SELECT id FROM products WHERE slug = ${esc(d.slug)}),`);
    push(`  NULL,`);
    push(`  ${esc(mName)},`);
    push(`  ${num(m.factor)},`);
    push(`  ${json(mCondition)},`);
    push(`  ${num(m.priority || 0)}`);
    push(`);`);
  }
  if ((d.multipliers || []).length) push('');

  // ---- addons + addon rate rows ------------------------------------------
  for (const addon of (d.addons || [])) {
    push(`-- addon: ${addon.key}`);
    push(`INSERT INTO product_addons (product_id, key, name, pricing_mode, flat_premium, triggered_by, available_if, dimensions)`);
    push(`VALUES (`);
    push(`  (SELECT id FROM products WHERE slug = ${esc(d.slug)}),`);
    push(`  ${esc(addon.key)},`);
    push(`  ${esc(addon.name)},`);
    push(`  ${esc(addon.pricing_mode)},`);
    push(`  ${num(addon.premium ?? addon.flat_premium ?? null)},`);
    push(`  ${json(addon.triggered_by || null)},`);
    push(`  ${json(addon.available_if || null)},`);
    push(`  ${json(addon.dimensions || null)}`);
    push(`);`);
    push('');

    for (const r of (addon.rates || [])) {
      const { premium, manual_quote, ...dims } = r;
      push(`INSERT INTO product_addon_rate_rows (addon_id, dimension_values, premium, manual_quote)`);
      push(`VALUES (`);
      push(`  (SELECT id FROM product_addons WHERE product_id = (SELECT id FROM products WHERE slug = ${esc(d.slug)}) AND key = ${esc(addon.key)}),`);
      push(`  ${json(dims)},`);
      push(`  ${num(premium ?? null)},`);
      push(`  ${bool(manual_quote || false)}`);
      push(`);`);
    }
    if ((addon.rates || []).length) push('');
  }

  // ---- eligibility rules -------------------------------------------------
  for (let ri = 0; ri < (d.eligibility || []).length; ri++) {
    const rule      = d.eligibility[ri];
    const ruleName  = rule.name || rule.reason?.slice(0, 60) || `rule_${ri}`;
    const ruleCond  = rule.condition || rule.if || {};
    push(`INSERT INTO product_eligibility_rules (product_id, name, condition, action, reason, priority)`);
    push(`VALUES (`);
    push(`  (SELECT id FROM products WHERE slug = ${esc(d.slug)}),`);
    push(`  ${esc(ruleName)},`);
    push(`  ${json(ruleCond)},`);
    push(`  ${esc(rule.action)},`);
    push(`  ${esc(rule.reason || null)},`);
    push(`  ${num(rule.priority || 0)}`);
    push(`);`);
  }
  if ((d.eligibility || []).length) push('');
}

push('COMMIT;');

const sql = lines.join('\n');
writeFileSync(OUT_FILE, sql);
console.log(`✓ Generated ${OUT_FILE} (${Math.round(sql.length/1024)}KB, ${lines.length} lines)`);
